		public PostScriptArray ToArray()
		{
			PostScriptArray postScriptArray = new PostScriptArray(this.sids.Length);
			for (int i = 0; i < this.sids.Length; i++)
			{
				postScriptArray.Add(this.sids[i]);
			}
			return postScriptArray;
		}

		public string GetGlyphName(ICFFFontFile fontFile, ushort index)
		{
			if ((int)index >= this.sids.Length)
			{
				return ".notdef";
			}
			return fontFile.ReadString(this.sids[(int)index]);
		}

