		public static string CreateFontKey(string font)
		{
			return font.Replace(" ", string.Empty).ToLower();
		}

		public static bool TryGetFontFamily(string font, out string fontFamily)
		{
			string text = PredefinedFontFamilies.CreateFontKey(FontsHelper.StripFontName(font));
			if (PredefinedFontFamilies.mapping.TryGetValue(text, out fontFamily))
			{
				return true;
			}
			if (text.Contains("arial"))
			{
				fontFamily = "Arial";
				return true;
			}
			if (text.Contains("times"))
			{
				fontFamily = "Times New Roman";
				return true;
			}
			return false;
		}

		static void RegisterFontFamily(string fontFamily)
		{
			string key = PredefinedFontFamilies.CreateFontKey(fontFamily);
			PredefinedFontFamilies.mapping[key] = fontFamily;
		}

		static void RegisterFontFamily(string fontFamily, string realFontFamily)
		{
			string key = PredefinedFontFamilies.CreateFontKey(fontFamily);
			PredefinedFontFamilies.mapping[key] = realFontFamily;
		}

		static readonly Dictionary<string, string> mapping = new Dictionary<string, string>();
